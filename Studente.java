
public class Studente {
	private String Nome;
	private String Cognome;

	//costruttore
	public Studente(String nome, String cognome) {
		super();
		Nome = nome;
		Cognome = cognome;
	}
	

	
//get and set
	
	public String getNome() {
		return Nome;
	}
	public void setNome(String nome) {
		Nome = nome;
	}
	public String getCognome() {
		return Cognome;
	}
	public void setCognome(String cognome) {
		Cognome = cognome;
	}


}





